// OPERADORES DE DESIGUALDADE

// DESIGUALDADE SIMPLES (APENAS O VALOR)

let idade = 25;
let salario = 5500.0;

console.log("DESIGUALDADE SIMPLES");
console.log(idade != 20, salario != 5500.0);
console.log(idade != "25", salario != "5500.0");

// DESIGUALDADE ESTRITA (O VALOR E O TIPO)

console.log("DESIGUALDADE ESTRITA");
console.log(idade !== 20, salario !== 5500.0);
console.log(idade !== "25", salario !== "5500.0");
